int	ft_isalpha_perc(int c)
{
	return (((c >= 'A' && c <= 'Z') || (c >= 'a' && c <= 'z') \
	|| c == '%') ? 1 : 0);
}

int	ft_isposdigit(int c)
{
	return ((c >= '1' && c <= '9') ? 1 : 0);
}
